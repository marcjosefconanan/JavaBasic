
//Dati due caratteri, restituire la somma dei loro valori ASCII
public class Casting4 {
    public static void main (String [] args ){
        char primaLettera = 'a';
        char secondaLettera = 'b';

        int ascii = (int) primaLettera + (int) secondaLettera;

        System.out.println("this is the Ascii value : " + ascii);
    }

}
